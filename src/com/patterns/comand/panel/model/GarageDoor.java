package com.patterns.comand.panel.model;

public class GarageDoor {
  public void up() {
    System.out.println("GarageDoor is up");
  }

  public void down() {
    System.out.println("GarageDoor is down");
  }

  public void stop() {
    System.out.println("GarageDoor is stop");
  }

  public void lightOn() {
    System.out.println("GarageDoor is light On");
  }

  public void lightOff() {
    System.out.println("GarageDoor is light Off");
  }
}
